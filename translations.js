function getCurrentLanguage() {
  return localStorage.getItem('hpl_language') || 'ru';
}

function setLanguage(lang) {
  if (lang === 'ru' || lang === 'en') {
    localStorage.setItem('hpl_language', lang);
    window.location.reload();
  }
}

function t(key) {
  const lang = getCurrentLanguage();
  return window.TRANSLATIONS[lang]?.[key] || window.TRANSLATIONS.ru[key] || key;
}
